#addition
def add(x, y)
  return x.to_f + y.to_f
end

#subtraction
def sub(x, y)
  return x.to_f - y.to_f
end

#multiplication
def mult(x, y)
  return x.to_f * y.to_f
end

#division
def div(x , y)
  return x.to_f / y.to_f
end

#exponentiation
def exp(x, y)
  return x.to_f ^ y.to_f
end